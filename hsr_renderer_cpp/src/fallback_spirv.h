// Validate SPIRV blobs and convert to vector<u32>
inline bool validateSpirv(const uint32_t* data, size_t byteSize, std::vector<u32>& out) {
    if (byteSize < 20 || (byteSize & 3)) return false;
    if (data[0] != 0x07230203) return false;
    size_t words = byteSize / 4;
    out.assign(data, data + words);
    return true;
}

